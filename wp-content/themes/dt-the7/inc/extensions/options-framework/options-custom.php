function optionsframework_presets_data( $id ) {
    static $presets = null;

    if ( null === $presets ) {
		
		$presets = array();

		foreach ( optionsframework_get_presets_list() as $fname=>$thumb ) {
			
			$file = OPTIONS_FRAMEWORK_PRESETS_DIR . $fname . '.php';
			
			if ( is_readable( $file ) ) {
				include_once( $file );
			}
		}
	}

    if ( isset( $presets[ $id ] ) ) {
        return $presets[ $id ];
    }

    return array();
}

